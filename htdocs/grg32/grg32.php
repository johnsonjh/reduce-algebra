			<div id="main">
				<p>The computer algebra system GRG is designed to make calculation in differential geometry and field theory as simple and natural as possible. GRG is based on the computer algebra system REDUCE but GRG has its own simple input language whose commands resemble short English phrases.</p>
				<p>GRG understands tensors, spinors, vectors, differential forms and knows all standard operations with these quantities. Input form for mathematical expressions is very close to traditional mathematical notation including Einstein summation rule. GRG knows covariant properties of the objects: one can easily raise and lower indices, compute covariant and Lie derivatives, perform coordinate and frame transformations etc. GRG works in any dimension and allows one to represent tensor quantities with respect to holonomic, orthogonal and even any other arbitrary frame.</p>
				<p>One of the key features of GRG is that it knows a large number of built-in usual field-theoretical and geometrical quantities and formulas for their computation providing ready solutions to many standard problems.</p>
				<p>Another unique feature of GRG is that it can export results of calculations into other computer algebra system such as Maple, Mathematica, Macsyma or REDUCE in order to use these systems to proceed with analysis of the data. The LaTeX output format is supported as well. GRG is compatible with the REDUCE graphics shells providing nice book-quality output with Greek letters, integral signs etc.</p>
				<p>The main built-in GRG capabilities are:</p>
				<ul>
					<li>Connection, torsion and nonmetricity.</li>
					<li>Curvature.</li>
					<li>Spinorial formalism.</li>
					<li>Irreducible decomposition of the curvature, torsion, and nonmetricity in any dimension.</li>
					<li>Einstein equations.</li>
					<li>Scalar field with minimal and non-minimal interaction.</li>
					<li>Electromagnetic field.</li>
					<li>Yang-Mills field.</li>
					<li>Dirac spinor field.</li>
					<li>Geodesic equation.</li>
					<li>Null congruences and optical scalars.</li>
					<li>Kinematics for time-like congruences.</li>
					<li>Ideal and spin fluid.</li>
					<li>Newman-Penrose formalism.</li>
					<li>Gravitational equations for the theory with arbitrary gravitational Lagrangian in Riemann and Riemann-Cartan spaces.</li>
				</ul>
				<p>The detailed documentation including complete manual and short reference guide is provided.</p>
