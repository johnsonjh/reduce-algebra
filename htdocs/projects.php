				<h2>Projects related to REDUCE</h2>
				<p>
					There are a number of independent projects that use REDUCE in various ways. These include:
				</p>
				<ul>
					<li>the <a href="http://www.alltypes.de/">ALLTYPES</a> algebraic language and type system;
					</li>
					<li>the <a href="http://axiom-wiki.newsynthesis.org/">MathAction</a> collaborative web interface for Axiom, REDUCE and other computer algebra systems;
					</li>
					<li>the <a href="http://mentorproducts.com/">Mentor Mathematics Advisor</a> for mathematics education;
					</li>
					<li><a href="http://mtt.sourceforge.net/">MTT</a>, a set of tools for modeling dynamic physical systems using the bond-graph methodology<!--.
  Authors: <a href="http://www.mech.gla.ac.uk/~gbevan/">Geraint Bevan</a> &amp; <a href="http://www.mech.gla.ac.uk/~peterg/sig.htm">Peter Gawthrop</a> -->;</li>
					<li><a href="https://github.com/chakravala/Reduce.jl">Reduce.jl</a>, an interface for calling REDUCE from the Julia language for symbolic manipulation.</li>
				</ul>
				<p>
					The following projects were distributed independently but are now available from the REDUCE SourceForge project:
				</p>
				<ul>
					<li><a href="http://www.redlog.eu/">Redlog</a> &ndash; Computing with Logic &ndash; is now an integral part of REDUCE;</li>
					<li><a href="http://cds.cern.ch/record/438562/files/0005081.pdf">Ortocartan</a> is a <a href="https://sourceforge.net/p/reduce-algebra/code/HEAD/tree/trunk/contrib/ortocartan/">set of programs</a> for algebraic calculations in relativity, implemented in the Lisp underlying REDUCE;</li>
					<li><a href="reduce-ide/reduce-ide.php">REDUCE IDE</a> is an <u>I</u>ntegrated <u>D</u>evelopment <u>E</u>nvironment for the <a href="http://www.gnu.org/software/emacs/emacs.html">GNU Emacs</a> editor.</li>
				</ul>
